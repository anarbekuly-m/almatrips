export interface GroupProgramStep {
  time: string;
  text: string;
}

/** Departure dates for one month. Days are strings to allow ranges like "24–25". */
export interface MonthDates {
  month: 'june' | 'july' | 'august';
  days: string[];
}

export interface GroupTourContent {
  name: string;
  tagline: string;
  metaTitle: string;
  metaDescription: string;
  teaser: string;
  intro: string[];
  locations: string[];
  included: string[];
  optional?: string[];
  program?: GroupProgramStep[];
  whatToBring: string[];
  notes?: string[];
  duration: string;
  season: string;
}

export interface GroupTour {
  slug: string;
  image: string;
  /** Optional extra photos for the slider. The cover `image` is always shown first. */
  gallery?: string[];
  emoji: string;
  /** per person, tenge */
  priceKzt: number;
  /** Overrides the plain price line when pricing is a range or has options. */
  priceNote?: { en: string; ru: string };
  /** true = departs daily; otherwise use `dates`. */
  daily?: boolean;
  /** Fixed departure dates by month. */
  dates?: MonthDates[];
  meetingPoint: { en: string; ru: string };
  en: GroupTourContent;
  ru: GroupTourContent;
}

